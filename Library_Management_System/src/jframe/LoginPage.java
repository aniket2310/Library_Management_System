    public void login(){
    String name = txt_username.getText();
    String password = txt_password.getText();
    
    try{
    Class.forName("com.mysql.cj.jdbc.Driver");
          Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_ms", "root", "2310");
          PreparedStatement pst = conn.prepareStatement("select * from users where name = ? and password = ?");
          
          pst.setString(1,name);
          pst.setString(2,password);
          ResultSet rs =  pst.executeQuery();
          if(rs.next()){
              JOptionPane.showMessageDialog(this,"Login Sucessful..ðŸ˜Š");
              HomePage home = new HomePage();
              home.setVisible(true);
              this.dispose();
          }else{
               JOptionPane.showMessageDialog(this,"Incorrect username or password");
          }
          
    }catch(Exception e){
    e.printStackTrace();
        }
    
    }
    
    

    
    
    
    
